print('*** Operadores de comparación (relacionales) ***')
a, b = 7, 5
print(f'Valor inicial a: {a}, b: {b}')

# Operador de igualdad == Devuelve un valor lógico (True/False)
resultado = a == b  # False
print(f'Resultado a == b es: {resultado}')

# Operador distinto !=
resultado = a != b  # True
print(f'Resultado a != b es: {resultado}')

# Operador mayor que >
resultado = a > b  # True
print(f'Resultado a > b es: {resultado}')

# Operador mayor o igual que >=
resultado = a >= b  # True
print(f'Resultado a >= b es: {resultado}')

# Operador menor que <
resultado = a < b  # False
print(f'Resultado a < b es: {resultado}')

# Operador menor o igual que <=
resultado = a <= b  # False
print(f'Resultado a <= b es: {resultado}')
