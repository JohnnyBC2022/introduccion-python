# Ejemplo de operadores aritméticos

# a = 10
# b = 3
a, b = 10, 3

# Suma  +
suma = a + b
print(f'Suma: {suma}')

# Resta -
resta = a - b
print(f'Resta: {resta}')

# Multiplicación *
multiplicacion = a * b
print(f'Multiplicación: {multiplicacion}')

# División /
division = a / b
print(f'División: {division}')

# División (entera) //
division_entera = a // b
print(f'División entera: {division_entera}')

# Módulo (residuo división) %
modulo = a % b
print(f'Módulo: {modulo}')

# Potencia **
potencia = a ** b  # 10 elevado a 3 -> 10*10*10
print(f'Potencia: {potencia}')
