# Programa para definir variables en Python
nombre = 'Jonathan'
edad = 44
pais = 'España'

# Mostramos en la consola los valores de cada variable
print('Nombre: ', nombre)
print('Edad: ', edad)
print('Pais: ', pais)

# Modificar los valores de una variable
nombre = 'Jonathan Baragaño'
edad: 25
print('Nombre: ', nombre)
print('Edad: ', edad)
print('Pais: ', pais)

# En Python el tipo es dinámico
edad = 'Veinticinco'
print('Edad: ', edad)

# Imprimir una variable no declarada anteriormente
telefono = '666123456'
print('Teléfono: ', telefono)
